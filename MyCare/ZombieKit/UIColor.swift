import UIKit

extension UIColor {
    public class func darkOrange() -> UIColor {
        return UIColor.init(colorLiteralRed: 255.0 / 255.0, green: 81.0 / 255.0, blue: 0.0, alpha: 1.0)
    }
  
    public class func darkGreen() -> UIColor {
        return UIColor.init(colorLiteralRed: 81.0 / 255.0, green: 149.0 / 255.0, blue: 156.0 / 255.0, alpha: 1.0)
    }
  
    public class func lightGreen() -> UIColor {
        return UIColor.init(colorLiteralRed: 230.0 / 255.0, green: 237.0 / 255.0, blue: 237.0 / 255.0, alpha: 1.0)
    }
  
    public class func darkYellow() -> UIColor {
        return UIColor.init(colorLiteralRed: 250.0 / 255.0, green: 181.0 / 255.0, blue: 27.0 / 255.0, alpha: 1.0)
    }

    public class func red() -> UIColor {
        return UIColor(red: 0xEF / 255.0, green: 0x44 / 255.0, blue: 0x5B / 255.0, alpha: 1.0)
    }

    public class func green() -> UIColor {
        return UIColor(red: 0x8D / 255.0, green: 0xC6 / 255.0, blue: 0x3F / 255.0, alpha: 1.0)
    }

    public class func blue() -> UIColor {
        return UIColor(red: 0x3E / 255.0, green: 0xA1 / 255.0, blue: 0xEE / 255.0, alpha: 1.0)
    }


    public class func lightBlue() -> UIColor {
        return UIColor(red: 0x9C / 255.0, green: 0xCF / 255.0, blue: 0xF8 / 255.0, alpha: 1.0)
    }

    public class func pink() -> UIColor {
        return UIColor(red: 0xF2 / 255.0, green: 0x6D / 255.0, blue: 0x7D / 255.0, alpha: 1.0)
    }

    public class func purple() -> UIColor {
        return UIColor(red: 0x9B / 255.0, green: 0x59 / 255.0, blue: 0xB6 / 255.0, alpha: 1.0)
    }

    public class func yellow() -> UIColor {
        return UIColor(red: 0xF1 / 255.0, green: 0xDF / 255.0, blue: 0x15 / 255.0, alpha: 1.0)
    }
}
